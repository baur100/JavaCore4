package ModelsYK;

public class Users {
    private int id;
    private String name;
    private String email;
    private boolean is_admin;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public boolean isIs_admin() {
        return is_admin;
    }
}
